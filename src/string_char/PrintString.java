package string_char;

public class PrintString {

	public static void main(String[] args) {
		System.out.println("abc");
		System.out.println("abc".length());
		System.out.println(" abc  ".trim());
		
		System.out.println(3 + 5 + "Java");
		System.out.println("Java" + 3 + 5);
		System.out.println("Java" + (3 + 5));
	}

}
